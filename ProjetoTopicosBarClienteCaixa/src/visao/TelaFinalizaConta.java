    private void getPedidos(){
        try{
            DefaultTableModel modeloTable = (DefaultTableModel) getTblPedidos().getModel();

            while (modeloTable.getRowCount() > 0) {
                modeloTable.removeRow(0);
            }

            for (Pedido p : listaPedido) {
                modeloTable.addRow(new Object[]{p.getnPedido(), p.getMesa().getNumTerminal(), p.getMesa().getNome(), p.getValorPedido()});
            }

        } catch (Exception e) {
            System.out.println("Erro: Mensagem: " + e.getMessage());
        }
    }
