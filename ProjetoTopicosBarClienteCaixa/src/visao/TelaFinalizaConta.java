    private void btnSolicitarPagamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSolicitarPagamentoActionPerformed
        if (getTblPedidos().getSelectedRow() != -1) {
            int linha = getTblPedidos().getSelectedRow();
            
            for (Pedido p : listaPedido) {
                if (p.getMesa().getNome().equals(getTblPedidos().getValueAt(linha, 2))) {
                    TelaFechaConta tFecharConta = new TelaFechaConta(p, caixa);
                    tFecharConta.setVisible(true);
                    this.dispose();
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um pedido para ser finalizado!");
        }
    }//GEN-LAST:event_btnSolicitarPagamentoActionPerformed

