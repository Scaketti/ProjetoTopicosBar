    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        if (pAlter == null) {
            Produto p = new Produto();
            p.setNome(txtNomeProduto.getText());
            p.setPreco(Float.parseFloat(txtPreco.getText()));
            p.setQtd(Integer.parseInt(txtQtd.getText()));

            try { //Tenta se conectar com o servidor
                ServidorBarInterface servidor = (ServidorBarInterface) Naming.lookup("rmi://" + "127.0.0.1" + ":" + "1099" + "/bar");

                int validacao = servidor.insereProduto(p, c.getNome());

                if (validacao == 0) {
                    JOptionPane.showMessageDialog(null, "Produto inserido com sucesso!");
                    getProdutos();

                    this.dispose();
                }

            } catch (Exception e) {
                System.out.println("Erro: Mensagem: " + e.getMessage());
            }
        }else{
            pAlter.setNome(txtNomeProduto.getText());
            pAlter.setPreco(Float.parseFloat(txtPreco.getText()));
            pAlter.setQtd(Integer.parseInt(txtQtd.getText()));
            
            try { //Tenta se conectar com o servidor
                ServidorBarInterface servidor = (ServidorBarInterface) Naming.lookup("rmi://" + "127.0.0.1" + ":" + "1099" + "/bar");

                int validacao = servidor.alteraProduto(pAlter, c.getNome());

                if (validacao == 0) {
                    JOptionPane.showMessageDialog(null, "Produto alterado com sucesso!");
                    getProdutos();

                    this.dispose();
                }

            } catch (Exception e) {
                System.out.println("Erro: Mensagem: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

    public void getProdutos() {
        try {
            ServidorBarInterface servidor = (ServidorBarInterface) Naming.lookup("rmi://" + "127.0.0.1" + ":" + "1099" + "/bar");
            ArrayList<Produto> pBusca = servidor.pesquisaProduto(c.getNome());

            DefaultTableModel modeloTable = (DefaultTableModel) tManter.getTblProdutos().getModel();

            while (modeloTable.getRowCount() > 0) {
                modeloTable.removeRow(0);
            }

            for (Produto p : pBusca) {
                modeloTable.addRow(new Object[]{p.getNome(), p.getQtd(), p.getPreco()});
            }

        } catch (Exception e) {
            System.out.println("Erro: Mensagem: " + e.getMessage());
        }

    }

